/**
 * 身份证正则
 *
 * @since 0.0.6
 * @example
 * ```ts
 * idCardRegexp.test('142701199304042345');
 * // => true
 * ```
 * */
const idCardRegexp = /(^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$)|(^[1-9]\d{5}\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{2}[0-9Xx]$)/;

export default idCardRegexp;
